package org.javaguru.mentor.lesson_7.homework.day_4.solutions;

class CashReceipt {

    private String number;
    private double sum;

    public CashReceipt(String number, double sum) {
        this.number = number;
        this.sum = sum;
    }

    public String getNumber() {
        return number;
    }

    public double getSum() {
        return sum;
    }

}
